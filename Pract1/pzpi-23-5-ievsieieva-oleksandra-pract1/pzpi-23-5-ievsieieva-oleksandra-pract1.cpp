#include <iostream>
#include <string>

using namespace std;

// =========================================================================================
// ПІДСИСТЕМИ
// =========================================================================================

// ГЕНЕРОВАНО ШІ: Реалізовано вивід на консоль для кожного методу підсистеми для демонстрації.

class AudioSystem {
public:
    // Встановлює рівень гучності аудіосистеми
    void setVolume(int v) { 
        // ГЕНЕРОВАНО ШІ: Реалізація логіки під запит користувача
        cout << "Аудіосистема: Встановлено рівень гучності на " << v << "%" << endl;
    }
};

class VideoProjector {
public:
    // Вмикає відеопроектор для відображення контенту
    void turnOn() { 
        // ГЕНЕРОВАНО ШІ: Реалізація логіки під запит користувача
        cout << "Відеопроектор: Увімкнено для відображення контенту." << endl;
    }

    void turnOff() {
        // ГЕНЕРОВАНО ШІ: Додано для повноти демонстрації Фасаду
        cout << "Відеопроектор: Вимкнено." << endl;
    }
};

class LightControl {
public:
    // Зменшує яскравість освітлення для комфортного перегляду
    void dimLights() { 
        // ГЕНЕРОВАНО ШІ: Реалізація логіки під запит користувача
        cout << "Керування світлом: Яскравість освітлення зменшено." << endl;
    }

    void turnOnLights() {
        // ГЕНЕРОВАНО ШІ: Додано для повноти демонстрації Фасаду
        cout << "Керування світлом: Увімкнено основне освітлення." << endl;
    }
};

// =========================================================================================
// КЛАС ФАСАДУ
// =========================================================================================

// ГЕНЕРОВАНО ШІ: Створено цей клас для об'єднання підсистем та клієнтського коду
// згідно з принципами патерна Фасад.

class HomeCinemaFacade {
private:
    AudioSystem* amp;
    VideoProjector* proj;
    LightControl* lights;

public:
    HomeCinemaFacade() {
        amp = new AudioSystem();
        proj = new VideoProjector();
        lights = new LightControl();
    }

    ~HomeCinemaFacade() {
        delete amp;
        delete proj;
        delete lights;
    }

    // ГЕНЕРОВАНО ШІ: Методи, що автоматизують роботу з декількома підсистемами
    void prepareForMovie() {
        cout << "--- Підготовка системи домашнього кінотеатру ---" << endl;
        lights->dimLights();
        proj->turnOn();
        amp->setVolume(20); 
    }

    void watchMovie() {
        cout << "--- Розпочинаємо перегляд фільму! ---" << endl;
        amp->setVolume(70); 
    }

    void endMovie() {
        cout << "--- Завершуємо перегляд. Повернення до початкового стану ---" << endl;
        amp->setVolume(10); 
        proj->turnOff();
        lights->turnOnLights();
    }
};

// =========================================================================================
// КЛІЄНТСЬКИЙ КОД
// =========================================================================================

int main() {
    HomeCinemaFacade cinema;

    // Підготувати систему до перегляду
    cinema.prepareForMovie();

    // Запустити сам перегляд
    cinema.watchMovie();

    // Завершити сеанс і повернути все у початковий стан
    cinema.endMovie();

    return 0;
}
